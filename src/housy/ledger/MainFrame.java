  private void setLedgerTableModel(Ledger ledger) {
    jTable1.setModel(new LedgerTableModel(ledger));
    jTable1.getColumnModel().getColumn(LedgerTableModel.VALUE_INDEX).setCellRenderer(new DefaultTableCellRenderer() {
      @Override
      public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
        Component comp = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
        ((JLabel) comp).setHorizontalAlignment(SwingUtilities.TRAILING);
        return comp;
      }
    });
  }

