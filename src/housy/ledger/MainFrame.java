  private void save() {
    if (saveFile == null) {
      saveAs();
      return; //saveAs() calls save() and we don't want it to get recursive now do we?
    }
    
    try {
      Serializer.save(saveFile, ledger);
      edited = false;
    } catch (IOException e) {
      JOptionPane.showMessageDialog(this, "Failed to save the file!");
    }
  }
  
  private void exit() {
    if (edited) {
      int exitCode = JOptionPane.showConfirmDialog(this, "You have unsaved changes!\nWould you like to save them before exiting?");
      
      if (exitCode == JOptionPane.YES_OPTION) {
        save();
      }
    }
    
    System.exit(0);
  }
  
  private void newPage() {
    if (edited) {
      int exitCode = JOptionPane.showConfirmDialog(this, "You have unsaved changes!\nWould you like to save them before creating a new instance?");
      
      if (exitCode == JOptionPane.YES_OPTION) {
        save();
      }
    }
    
    ledger = new Ledger();
    saveFile = null;
    edited = false;
    setLedgerTableModel(ledger);
    reload();
  }
  
  private void load() {
    JFileChooser chooser = new JFileChooser();
    chooser.setFileFilter(new LedgerFileFilter());
    int exitCode = chooser.showOpenDialog(this);
    
    if (exitCode != JFileChooser.APPROVE_OPTION) {
      return;
    }
    
    File selectedFile = chooser.getSelectedFile();
    saveFile = selectedFile;
    ledger = Serializer.load(saveFile);
    setLedgerTableModel(ledger);
    reload();
  }
  
  private void saveAs() {
    JFileChooser chooser = new JFileChooser();
    chooser.setFileFilter(new LedgerFileFilter());
    int exitCode = chooser.showSaveDialog(this);
    
    if (exitCode != JFileChooser.APPROVE_OPTION) {
      return;
    }
    
    File selectedFile = chooser.getSelectedFile();
    
    if (!selectedFile.getName().endsWith(LedgerFileFilter.EXTENSION)) {
      selectedFile = new File(selectedFile.getAbsolutePath() + LedgerFileFilter.EXTENSION);
    }
    
    if (selectedFile.exists()) {
      int ensurance = JOptionPane.showConfirmDialog(this, "This file already exists!\nAre you sure you want to overwrite it?");
      
      if (ensurance != JOptionPane.YES_OPTION) {
        return;
      }
    }
    
    saveFile = selectedFile;
    save();
  }
  
