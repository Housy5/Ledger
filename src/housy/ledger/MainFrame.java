  private void showTransactionCreator(int mode) {
    TransactionCreator creator = new TransactionCreator(this, mode);
    creator.setVisible(true);
    int code = creator.getExitCode();
    
    if (code != TransactionCreator.SUCCESS) {
      return;
    }
    
    ledger.record(creator.getTransaction());
    reload();
    edited = true;
  }
  
  private void reload() {
    jTable1.revalidate();
    jTable1.repaint();
    DecimalFormat format = new DecimalFormat("#,##0.00");
    String total = format.format(ledger.getTotal().doubleValue());
    jLabel1.setText("Total:    " + total +  " " + ledger.getCurrencySymbol());
  }
  
